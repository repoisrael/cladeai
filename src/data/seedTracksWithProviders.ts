// Get seed SQL for inserting tracks
export function generateSeedSQL(): string {
  const trackInserts = seedTracksWithProviders.map((track, index) => `
    INSERT INTO public.tracks (
      external_id, provider, title, artist, album, duration_ms, isrc, cover_url,
      detected_key, detected_mode, progression_roman, loop_length_bars,
      spotify_id, youtube_id, url_spotify_web, url_spotify_app, url_youtube,
      energy, danceability, valence
    ) VALUES (
      '${track.spotify_id || track.youtube_id || `seed-${index}`}',
      'spotify',
      '${track.title.replace(/'/g, "''")}',
      '${track.artist.replace(/'/g, "''")}',
      ${track.album ? `'${track.album.replace(/'/g, "''")}'` : 'NULL'},
      ${track.duration_ms || 'NULL'},
      ${track.isrc ? `'${track.isrc}'` : 'NULL'},
      ${track.cover_url ? `'${track.cover_url}'` : 'NULL'},
      ${track.detected_key ? `'${track.detected_key}'` : 'NULL'},
      ${track.detected_mode ? `'${track.detected_mode}'` : 'NULL'},
      ${track.progression_roman ? `ARRAY[${track.progression_roman.map(p => `'${p}'`).join(', ')}]` : 'NULL'},
      ${track.loop_length_bars || 'NULL'},
      ${track.spotify_id ? `'${track.spotify_id}'` : 'NULL'},
      ${track.youtube_id ? `'${track.youtube_id}'` : 'NULL'},
      ${track.spotify_id ? `'https://open.spotify.com/track/${track.spotify_id}'` : 'NULL'},
      ${track.spotify_id ? `'spotify:track:${track.spotify_id}'` : 'NULL'},
      ${track.youtube_id ? `'https://www.youtube.com/watch?v=${track.youtube_id}'` : 'NULL'},
      ${track.energy || 'NULL'},
      ${track.danceability || 'NULL'},
      ${track.valence || 'NULL'}
    ) ON CONFLICT DO NOTHING
    RETURNING id
  `).join(';\n');

  return trackInserts;
}
